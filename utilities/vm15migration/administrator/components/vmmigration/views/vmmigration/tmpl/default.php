<h1> Virtuemart 1.5.0 Migration Utility</h1>
This utility is designed to migrate your Virtuemart 1.1.x installation to the new 1.5.0 platform.<br />
Once your Virtuemart installation is migrated, future updates can be done throught the update tool included in Virtuemart.
<br /><br />
<h3>3rd Party Extensions/Addons:</h3>
Please be aware any 3rd party extensions or addons will be lost during the 
migration process.  Be sure you have a backup of them to re-apply after the migration.</b>
<br /><br />
<h3>Please note:</h3>
This process may take several minutes...do not interupt this process!</b>
